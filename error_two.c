int	ft_check_special(char *var)
{
	int	i;

	i = -1;
	while (var[++i])
	{
		if ((var[i] >= 0 && var[i] <= 47) || (var[i] >= 58 && var[i] <= 64) \
			|| (var[i] >= 91 && var[i] <= 94) || \
			(var[i] >= 123 && var[i] <= 127))
			return (1);
	}
	return (0);
}
